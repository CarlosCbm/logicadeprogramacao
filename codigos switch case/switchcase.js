console.log("Qual sistema operacional você quer usar?\n1 - Terminal Linux\n2 - CMD Windows");

let sistema = Number(prompt("Digite sua escolha: "));

if (sistema === 1) {
  console.log("\nVocê escolheu Terminal Linux");

  let comandos = prompt("Digite o comando para saber o que ele faz: ");
  switch (comandos) {
    case "cd ..":
      console.log("Quando você executa o 'cd ..', está dizendo ao terminal para voltar um nível na hierarquia de diretórios.");
      break;

    case "mkdir":
      console.log("Isso cria um diretório com o nome especificado no local atual.");
      break;

    case "ls":
      console.log("Sem argumentos, ele lista os arquivos e pastas no diretório atual.");
      break;

    case "rm":
      console.log("Remover um arquivo.");
      break;

    case "clear":
      console.log("Ele não apaga nada do sistema, nem fecha o terminal — só limpa a exibição atual.");
      break;

    case "cp":
      console.log("Copiar um arquivo para outro lugar (ou com outro nome).");
      break;

    case "mv":
      console.log("Serve para mover ou renomear arquivos e diretórios.");
      break;

    case "cat":
      console.log("Exibir o conteúdo de arquivos no terminal.");
      break;

    case "echo":
      console.log("O 'echo' serve para exibir mensagens no terminal ou gravar texto em arquivos.");
      break;

    default:
      console.log("Não temos esse comando.");
  }

} else if (sistema === 2) {
  console.log("\nVocê escolheu o CMD Windows");

  let comandos = prompt("Digite o comando para saber o que ele faz: ");
  switch (comandos) {
    case "mkdir":
      console.log("Serve para criar uma nova pasta (ou diretório).");
      break;

    case "dir":
      console.log("É usado para listar o conteúdo de um diretório (pastas e arquivos).");
      break;

    case "cd":
      console.log("É usado para navegar entre pastas (diretórios).");
      break;

    case "cd ..":
      console.log("Ele volta um nível na estrutura de pastas.");
      break;

    case "del":
      console.log("É usado para deletar (apagar) arquivos.");
      break;

    case "cls":
      console.log("Apaga todo o conteúdo visível da janela do CMD — só limpa a tela.");
      break;

    case "echo":
      console.log("Ele exibe mensagens no terminal ou controla a exibição de comandos em scripts.");
      break;

    case "copy":
      console.log("É usado para copiar arquivos de um lugar para outro.");
      break;

    case "move":
      console.log("É usado para mover arquivos ou pastas, ou até renomear.");
      break;

    case "rmdir":
      console.log("Serve para remover pastas (diretórios) no CMD do Windows.");
      break;

    case "exit":
      console.log("Ele fecha a janela do terminal (ou encerra o processo do CMD).");
      break;

    default:
      console.log("Não temos esse comando.");
  }

} else {
  console.log("Não temos esse sistema operacional.");
}


